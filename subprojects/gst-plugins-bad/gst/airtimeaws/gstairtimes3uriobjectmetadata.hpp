#pragma once

#include <cstdint>
#include <string>

namespace gst::airtime
{

struct S3URIObjectMetadata {
    /// @brief The S3 bucket name.
    std::string bucket;

    /// @brief The S3 object key.
    std::string key;

    /// @brief The content length of the S3 object in bytes.
    std::uint64_t content_length{0};

    /// @brief The MD5 hash (entity tag) of the S3 object.
    std::string entity_tag;

    /// @brief The version ID of the S3 object, if applicable.
    std::string version_id;

    /// @brief The expiration date of the S3 object, if applicable.
    std::string expiration;

    /// @brief The last modified date of the S3 object.
    std::string last_modified;

    auto operator<=>(const S3URIObjectMetadata&) const = default;
};

} // namespace gst::airtime